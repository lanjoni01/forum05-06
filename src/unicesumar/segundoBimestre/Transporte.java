package unicesumar.segundoBimestre;

public class Transporte {

	private String tipo;
	private String modelo;

	public Transporte(String tipo, String modelo) {
		this.tipo = tipo;
		this.modelo = modelo;
	}

	public String getTipo() {
		return tipo;
	}

	public String getModelo() {
		return modelo;
	}
	
	

}
